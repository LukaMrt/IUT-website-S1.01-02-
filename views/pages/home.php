<h2><em>À retenir</em></h2>

<div class="cards">

    <div>
        <h3>Temps de formation</h3>
        <p>1 800h de cours en plus des projets tutorés réparties en 3 ans de BUT.</p>
    </div>

    <div>
        <h3>Matières enseignées</h3>
        <p>
            Développement web, Conception de bases de données, Mathématiques, SQL, Langage C, Bases de l'économie,
            Algorithmique, Communication, Architecture des ordinateurs, Systèmes d'exploitation, Projet personnel et
            professionnel, Git.
        </p>
    </div>

    <div>
        <h3>Horaires hebdomadaires</h3>
        <p>
            Environ 30h de cours par semaine. Les cours ont lieu de 8h à 18h avec une pause de 12h à 14h et sont tous
            des
            cours de 2h. Le jeudi après-midi est libre.
        </p>
    </div>

    <div>
        <h3>Types de cours</h3>
        <p>Il y a 3 types de cours : les cours en amphithéâtre avec l'ensemble de la promotion (130 étudiants), les
            Travaux Dirigés (TD) en groupe (25 étudiants) et le Travaux Pratiques (TP) en demi-groupe (12
            étudiants).</p>
    </div>

    <div>
        <h3>Prix de la formation</h3>
        <p>
            La formation en elle-même est gratuite. Il faut cependant payer sa Contribution à la Vie Étudiante du Campus
            (CVEC, 90€) et son inscription à l'université (170€). Les étudiants boursiers sont exonérés de ces
            coûts.
        </p>
    </div>

    <div>
        <h3>Organisation de l'année</h3>
        <p>
            La formation est découpée en semestres indépendants. Les matières changent chaque semestre. Les groupes
            d'étudiants sont aussi remélangés (information à vérifier).
        </p>
    </div>

</div>


